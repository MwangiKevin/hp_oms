CREATE TABLE `users` (
  `id` int(11) unsigned NOT NULL AUTO_INCREMENT,
  `active` tinyint(1) unsigned DEFAULT NULL,
  `last_login` int(11) unsigned DEFAULT NULL,
  `ip_address` varchar(15) NOT NULL,
  `surname` varchar(50) DEFAULT NULL,
  `other_names` varchar(100) DEFAULT NULL,
  `username` varchar(100) NOT NULL,
  `birthday` varchar(20) DEFAULT NULL,
  `gender` int(11) DEFAULT NULL,
  `sub_county_id` int(11) NOT NULL,
  `phone` varchar(12) DEFAULT NULL,
  `email` varchar(100) NOT NULL,
  `password` varchar(255) NOT NULL,
  `salt` varchar(255) DEFAULT NULL,
  `remember_code` varchar(40) DEFAULT NULL,
  `activation_code` varchar(40) DEFAULT NULL,
  `forgotten_password_code` varchar(40) DEFAULT NULL,
  `forgotten_password_time` int(11) unsigned DEFAULT NULL,
  `created_on` int(11) unsigned NOT NULL,
  PRIMARY KEY (`id`),
  KEY `fk_users_sub_counties1_idx` (`sub_county_id`),
  CONSTRAINT `fk_users_sub_counties1` FOREIGN KEY (`sub_county_id`) REFERENCES `sub_counties` (`sub_county_id`) ON DELETE NO ACTION ON UPDATE NO ACTION
) ENGINE=InnoDB DEFAULT CHARSET=utf8